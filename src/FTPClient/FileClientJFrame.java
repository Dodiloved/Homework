    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         ip=jTextField1.getText();//取出IP地址数据
        port=jTextField2.getText();//取出端口号数据
    try {
        IP =new FileDialogClient(ip,port);//发起TCP连接 
        jTextArea2.append("服务器连接成功.\r\n");
    } catch (IOException ex) {

      jTextArea2.append("服务器连接失败.\r\n");

    }
                Thread receiver=new Thread(){ //定义一个专门用来接收信息的线程。

           @Override

           public void run(){

              String msg=null;

              while((msg=IP.receive())!=null){

                   jTextArea2.append(msg+"\n");

              }

                  jTextArea2.append("对话已关闭！\n");

               }  };       

            receiver.start(); //启动该线程
    }//GEN-LAST:event_jButton1ActionPerformed

